    private void ValidUser(){
        try{
            cone = new Conect("sucursal");
            String sql = "select * from usuarios where Nombre ='" + txtUser.getText().trim() + "' and Pass ='" + txtPass.getText() + "'";
            con = cone.getConexion();
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            if(rs.next()){
                new Princiapl(rs.getInt("Acces"),rs.getString("Nombre")).setVisible(true);
                con.close();
                this.dispose();
            } else{
                met.Msg("Error datos incorrectos");
                met.Clean(jPanel1);
            }
        }catch(Exception e){
            met.Msg("Error"+e);
        }
    }
    
